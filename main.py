MAP_W = 50; MAP_H = 50
CASTLE_POS = (25, 6)
TOWN_HAJIME = (20, 12); TOWN_LOGIC = (30, 22)
TOWN_ALGO = (18, 34); CAVE_GLITCH = (38, 42)
