def say(*args):
    """画面左上にデバッグ表示するヘルパ関数。

    使い方:
        say("こんにちは")             # → 画面左上に「こんにちは」が出る
        say("hp =", player["hp"])     # → 「hp = 45」のように出る

    最新 12 行までが画面左上にオーバーレイ表示される。
    print() より便利な点:
      - ゲーム画面に出るのでブラウザでも見える（Code Maker 環境向け）
      - 日本語が表示できる（仮名のみ）
    """
    if Game._instance is not None:
        Game._instance.say(*args)


def say_clear():
    """say バッファをクリアする。"""
    if Game._instance is not None:
        Game._instance._say_buffer.clear()


