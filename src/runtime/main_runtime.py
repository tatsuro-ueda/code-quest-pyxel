    def _wrap_text(self, text: str, max_chars: int = 28) -> list[str]:
        """簡易な折返し（CJK文字幅考慮なし、おおよその目安）。"""
        out: list[str] = []
        for raw_line in text.split("\n"):
            cur = ""
            for ch in raw_line:
                cur += ch
                if len(cur) >= max_chars:
                    out.append(cur)
                    cur = ""
            if cur:
                out.append(cur)
        return out or [""]

